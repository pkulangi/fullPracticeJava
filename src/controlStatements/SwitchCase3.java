package controlStatements;

public class SwitchCase3 {

	public static void main(String[] args)
	{
		int year;
		year=3;
		
		switch (year) {
		case 1:
		{
			System.out.println("1st year of Btech");
			break;
		}
		case 2:
		{
			System.out.println("2nd year of Btech");
			break;
		}
		case 3:
		{
			System.out.println("3rd year of Btech");
			break;
		}
		case 4:
		{
			System.out.println("Final year of Btech");
		}
		}
		

	}

}
