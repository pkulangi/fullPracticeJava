package controlStatements;

public class SwitchCase4 {

	public static void main(String[] args)
	{
		String company;
		company="tcs";
		
		switch (company) {
		case "wipro":
		{
			System.out.println("its a wipro company");
			break;
		}
		case "tcs":
		{
			System.out.println("Its a tcs company");
			break;
		}
		case "infosys":
		{
			System.out.println("its a infosys company");
			break;
		}
		}

	}

}
