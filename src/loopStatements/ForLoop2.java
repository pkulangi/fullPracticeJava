package loopStatements;

public class ForLoop2 {

	public static void main(String[] args)
	{
		// i want to write 1 to 100
		//start-->>1  end--->100 update by 1
		
		for(int i=1;i<=100;i++)
		{
			System.out.println(i);
		}
		
		// i want to print 1 to 1000
		//start-->1 End-->1000 update by 1
		
		for(int i=1;i<=1000;i++)
		{
			System.out.println(i);
		}

	}

}
